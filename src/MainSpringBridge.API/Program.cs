using MainSpringBridge.API.Extensions;

var builder = WebApplication.CreateBuilder(args);

// Add services
builder.Services.AddControllers();                          // MVC Controllers
builder.Services.AddEndpointsApiExplorer();                // Swagger endpoints
builder.Services.AddSwaggerGen();                          // Swagger UI
builder.Services.AddProjectServices();                     // Register app services like Dapper repo

var app = builder.Build();

// Enable Swagger UI in dev
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers(); // 🔥 This maps your controller-based endpoints

app.Run();
